#entrada de dados
numero_inteiro_um = int(input('Número inteiro 1: '))
numero_inteiro_dois = int(input('Número inteiro 2: '))

#processamento 
quociente = numero_inteiro_um / numero_inteiro_dois
resto = numero_inteiro_um % numero_inteiro_dois

#saída de dados
print(f'O quociente é {quociente} e o resto é {resto}')