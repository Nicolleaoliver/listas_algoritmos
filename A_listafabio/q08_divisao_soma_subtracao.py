#entrada de dados
numero_um = int(input('Número 1: '))
numero_dois = int(input('Número 2: '))

#processamento
soma = numero_um + numero_dois
subtracao = numero_um - numero_dois
divisao_soma_e_subtracao = soma / subtracao

print(f'A divisão da soma pela subtração dos números lidos é {divisao_soma_e_subtracao}')