def obter_numero_real(instrucoes = 'Digite um número real: '):
    numero = float(input(instrucoes))
    return numero


def obter_numero_inteiro(instrucoes = 'Digite um número inteiro: '):
    numero_inteiro = int(input(instrucoes))
    return numero_inteiro


